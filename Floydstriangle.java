
public class Floydstriangle {
public static void main(String[] args) {
	
	int z =1;
	for (int n = 1;n<=6;n++) {
		for( int c = 0; c < n; c++) {  //пока  с < 1(n) -      ;
			System.out.print(z+" ");  // - вывожу 1 раз    ;
			z++;
		}System.out.println();
	}
}
}

				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
		